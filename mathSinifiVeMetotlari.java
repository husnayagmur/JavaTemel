public class mathSinifiVeMetotlari {
    public static void main(String[] args) {
        System.out.println(Math.abs(-100));//mutlagni alir.
        System.out.println(Math.cbrt(8));//küp kokunu alir.
        System.out.println(Math.ceil(1.12));//b-kendine en yakin bir üst sayiya yuvarlar.
        System.out.println(Math.floor(1.12));//b-kendine en yakin tam sayiya yuvarlar.
        System.out.println(Math.max(100,150));//büyük sayiyi verir.
        System.out.println(Math.min(100,150));//kucuk sayiyi verir.
        System.out.println(Math.pow(2,3));//büyük sayiyi verir.
        System.out.println(Math.random()*10);//0-10 arasi rastgele.
        System.out.println(Math.round(10.99));//int türünde en yakin sayiya yuvarlar.
        System.out.println(Math.signum(-5));//sayinin isaretini verir
        System.out.println(Math.sqrt(4));//karekok sayiyi verir.

    }
}
